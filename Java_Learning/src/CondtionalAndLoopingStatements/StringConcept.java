package CondtionalAndLoopingStatements;

public class StringConcept {

	public static void main(String[] args) {
		
		String s = " yamini allampati ";
		System.out.println(s);
		System.out.println(s.length()); //output - integer
		System.out.println(s.indexOf('i'));//output - integer
		System.out.println(s.trim());//output - string
		System.out.println(s.substring(14));////output - string
		System.out.println(s.substring(2, 14)); // ending index always (index-1) ////output - string
		
		

	}

}
